import math

print("Factorial of 5 is {}".format(math.factorial(5)))

print("e to the power of 3 is {}".format(math.exp(3)))

print("Rounding of 4.5 to the upper value results in {}".format(math.ceil(4.5)))

print("GCD of 4, 6 is {}".format(math.gcd(4, 6)))

print("2 to the power of 30 is  {}".format(math.pow(2, 30)))